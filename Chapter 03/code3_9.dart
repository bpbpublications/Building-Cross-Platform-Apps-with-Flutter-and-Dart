main() {
  String color = 'red';
  switch (color) {
    case 'blue':
      print('COLOR IS BLUE');
      break;
    case 'red':
      print('COLOR IS RED');
      break;
    case 'yellow':
      print('COLOR IS YELLOW');
      break;
    default:
      print('NO COMPATIBLE COLOR');
      break;
  }
}
